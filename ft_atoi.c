int	ft_atoi(const char *str)
{
	int	tot;
	int	sign;

	sign = 1;
	tot = 0;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (*str != '\0')
	{
		if (*str < '0' || *str > '9')
			break ;
		tot += tot * 10 + (*str - '0');
		str++;
	}
	return (tot * sign);
}
